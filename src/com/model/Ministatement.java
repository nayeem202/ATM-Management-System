    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        DefaultTableModel model = (DefaultTableModel) statement.getModel();
        String date1 = ((JTextField) jDateChooser1.getDateEditor().getUiComponent()).getText();
        String date2 = ((JTextField) jDateChooser2.getDateEditor().getUiComponent()).getText();

        try {

            String sql = "SELECT * FROM transaction_tbl2 where  Account_Number ='" + myaccNumbr + "' and   Date between ? and ?";
            int rowCount = statement.getRowCount();
            for (int i = 0; i < rowCount; i++) {
                model.removeRow(0);
            }

            Connection conn = DatabaseConnector.connect();
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, date1);
            pst.setString(2, date2);

            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                int Sl = rs.getInt("Sl.");
                String account_number = rs.getString("Account_Number");
                String type = rs.getString("Transaction_Type");
                int amount = rs.getInt("Amount");
                String date = rs.getString("Date");
                model.addRow(new Object[]{Sl, account_number, type, amount, date});
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

